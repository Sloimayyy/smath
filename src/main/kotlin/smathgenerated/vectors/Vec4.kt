    fun eSum() = x + y + z + w
    fun elementProd() = x * y * z * w
    fun eProd() = x * y * z * w
    fun minElement() = min(min(x, y), min(z, w))
    fun eMin() = min(min(x, y), min(z, w))
    fun maxElement() = max(max(x, y), max(z, w))
    fun eMax() = max(max(x, y), max(z, w))
    fun indexOfMin() = when (eMin()) { x -> 0; y -> 1; z -> 2; else -> 3 }
    fun indexOfMax() = when (eMax()) { x -> 0; y -> 1; z -> 2; else -> 3 }
    fun manhattan(other: Vec4) = (this - other).abs().eSum()
    fun toArray() = floatArrayOf(x, y, z, w)
    fun lerp(other: Vec4, t: Float) = this * (1.0f - t) + other * t
    fun floor() = Vec4(floor(x), floor(y), floor(z), floor(w))
    fun ceil() = Vec4(ceil(x), ceil(y), ceil(z), ceil(w))
    fun round() = Vec4(round(x), round(y), round(z), round(w))
    fun fract() = mod(1.0f)

