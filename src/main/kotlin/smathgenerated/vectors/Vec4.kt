    operator fun get(idx: Short) = when (idx.toInt()) { 0 -> x; 1 -> y; 2 -> z; else -> w }
    operator fun get(idx: Int) = when (idx) { 0 -> x; 1 -> y; 2 -> z; else -> w }
    operator fun get(idx: Long) = when (idx.toInt()) { 0 -> x; 1 -> y; 2 -> z; else -> w }
