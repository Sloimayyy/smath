    fun lenSq() = dot(this)
    fun magSq() = dot(this)
    fun len() = sqrt(lenSq())
    fun mag() = sqrt(lenSq())
    fun dist(other: Vec4) = (this - other).len()
    fun distSq(other: Vec4) = (this - other).lenSq()
    fun normalize() = this / len()
    fun norm() = this / len()
    fun unit() = this / len()
    fun dir() = this / len()
