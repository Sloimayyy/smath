        fun new(x: Double, y: Double, z: Double, w: Double) = Vec4(x, y, z, w)

        fun fromArray(array: FloatArray): Vec4 {
            require(array.size == 4) { 
                "Inputted array isn't of the same dimensions as the vector we're trying to create. Expected 4, got ${array.size}."
            }
            return Vec4(array[0], array[1], array[2], array[3])
        }

