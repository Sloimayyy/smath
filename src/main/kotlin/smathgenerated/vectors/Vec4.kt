    constructor(x: Byte, y: Byte, z: Byte, w: Byte) : this(x.toFloat(), y.toFloat(), z.toFloat(), w.toFloat())
    constructor(value: Short) : this(value.toFloat(), value.toFloat(), value.toFloat(), value.toFloat())
    constructor(x: Short, y: Short, z: Short, w: Short) : this(x.toFloat(), y.toFloat(), z.toFloat(), w.toFloat())
    constructor(value: Int) : this(value.toFloat(), value.toFloat(), value.toFloat(), value.toFloat())
    constructor(x: Int, y: Int, z: Int, w: Int) : this(x.toFloat(), y.toFloat(), z.toFloat(), w.toFloat())
