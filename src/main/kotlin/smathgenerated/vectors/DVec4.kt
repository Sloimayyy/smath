    fun mod(other: DVec4) = DVec4(x.mod(other.x), y.mod(other.y), z.mod(other.z), w.mod(other.w))
    fun min(other: DVec4) = DVec4(min(x, other.x), min(y, other.y), min(z, other.z), min(w, other.w))
    fun max(other: DVec4) = DVec4(max(x, other.x), max(y, other.y), max(z, other.z), max(w, other.w))
    fun clamp(low: DVec4, high: DVec4) = DVec4(max(min(x, high.x), low.x), max(min(y, high.y), low.y), max(min(z, high.z), low.z), max(min(w, high.w), low.w))
    fun dot(other: DVec4) = x * other.x + y * other.y + z * other.z + w * other.w
    fun lenSq() = dot(this)
    fun len() = sqrt(lenSq())
    fun dist(other: DVec4) = (this - other).len()
    fun distSq(other: DVec4) = (this - other).lenSq()
    fun normalize() = this / len()
    fun dir() = this / len()
    fun elementSum() = x + y + z + w
    fun eSum() = x + y + z + w
    fun elementProd() = x * y * z * w
    fun eProd() = x * y * z * w
    fun minElement() = min(min(x, y), min(z, w))
    fun eMin() = min(min(x, y), min(z, w))
    fun maxElement() = max(max(x, y), max(z, w))
    fun eMax() = max(max(x, y), max(z, w))
    fun indexOfMin() = when (eMin()) { x -> 0; y -> 1; z -> 2; else -> 3 }
    fun indexOfMax() = when (eMax()) { x -> 0; y -> 1; z -> 2; else -> 3 }
    fun manhattan(other: DVec4) = (this - other).abs().eSum()
    fun toArray() = doubleArrayOf(x, y, z, w)
    fun lerp(other: DVec4, t: Double) = this * (1.0 - t) + other * t
    fun floor() = DVec4(floor(x), floor(y), floor(z), floor(w))
    fun ceil() = DVec4(ceil(x), ceil(y), ceil(z), ceil(w))
    fun round() = DVec4(round(x), round(y), round(z), round(w))
    fun fract() = mod(1.0)

